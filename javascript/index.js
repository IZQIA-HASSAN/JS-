// var in js
var name = "ali";
console.log(name)
var name = "ali"
console.log(name)
name = "ahmed"
console.log(name)

// var can be redeclared and reassigned and is function scoped.


// let in js 
let member = "ahmed"
console.log(member)
member = "hassan"
console.log(member)
let member = "khan"
console.log(member)

// let can be reassigened but cannot be redeclared and is block scoped 

// const in js 
const number  = 22 ;
console.log(number)
number = 34
console.log(number)

// const cannot be reassigned and cannot be redeclared and is  block scoped 